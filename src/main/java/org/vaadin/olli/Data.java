package org.vaadin.olli;

public class Data {
    private String a;
    private String b;
    private String c;

    public Data(String a, String b, String c) {
        this.a = a;
        this.b = b;
        this.c = c;
    }

    public String getA() {
        return a;
    }

    public String getB() {
        return b;
    }

    public String getC() {
        return c;
    }
}

